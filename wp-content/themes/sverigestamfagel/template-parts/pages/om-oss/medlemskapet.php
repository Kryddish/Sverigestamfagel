        <h2>Medlemsavgiften</h2>
            <p>Medlemsavgiften är 300 kronor per kalenderår. Medlemsavgiften är uppdelad i följande poster:</p>
            <p>50 kronor till Riksförbundet Svensk Fågelhobby (SF).</p>
            <p>210 kronor för tidningen Fågelhobby. Mer om tidningen kan du läsa nedan.</p>
            <p>40 kronor går lokalt till STF. Dessa pengar används bland annat till att hålla hemsidan uppe. Styrelsen arbetar ideellt.</p>
            <p>När du blir medlem i föreningen efter den 30/6, så har du möjlighet att betala bara halva avgiften för tidningen (105 kronor istället för 210 kronor). Den totala medlemsavgiften blir då  195 kronor.</p>
            <p>När du gör så kommer du enbart att få Medlemstidningen från och med att du har blivit medlem hos oss. Betalar du fullt medlemsavgiften på 300 kronor efter den 30/6, så skickas även årets tidigare utgåvor av fågelhobby tidningen hem till dig</p>
            <p>Är du redan medlem i en annan förening som är ansluten till Riksförbundet Svensk Fågelhobby betalar du 60 kronor i medlemsavgift. <a href="http://fagelhobby.nu/forbundet/foreningar/">Lista på anslutna föreningar</a></p>
            <p>Om fler personer inom samma familj, bosatta på samma adress blir medlemmar betalar en full medlemsavgift, övriga betalar reducerad föreningsavgift (familjemedlemskap), 70 kronor i medlemsavgift.</p>
            <p>Medlemsavgiften för kommande år ska vara inbetald på föreningens plusgirokonto senast den 10:e januari. (Till exempel medlemsavgift för år 2018 ska vara inbetald senast 2018-01-10)</p>
